import type { EncodingName, Match } from '../match';
import type { Context } from './index';
import { HighByteSBCS, NGramsPlusLang, caseFoldedByteMap } from './sbcs';

abstract class LegacySingleByte extends HighByteSBCS {
  constructor(
    private readonly encodingName: EncodingName,
    private readonly pairs: number[][],
    private readonly models: NGramsPlusLang[],
    private readonly evidence: number[] = [],
    private readonly evidenceBoost = 0,
  ) {
    super();
  }

  byteMap() {
    return caseFoldedByteMap(this.pairs);
  }

  ngrams() {
    return this.models;
  }

  name(): EncodingName {
    return this.encodingName;
  }

  match(det: Context): Match | null {
    const result = super.match(det);
    if (
      result &&
      this.evidenceBoost > 0 &&
      det.inputBytes.some((byte) => this.evidence.includes(byte))
    ) {
      result.confidence = Math.min(100, result.confidence + this.evidenceBoost);
    }
    return result;
  }
}

